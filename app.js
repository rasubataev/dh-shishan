function loadState() {
  try {
    const raw = localStorage.getItem(SK);
    if (raw) {
      const p = JSON.parse(raw);
      if (p && p.version >= 2) {
        p.version = 3;
        return p;
      }
    }
  } catch(e) {}
  const s = defaultState();
  saveStateSync(s);
  return s;
}

